export function getCategoryBySlug(slug) {
  return SEO_CATEGORIES.find((category) => category.slug === slug);
}

export function getPostBySlug(slug) {
  return BLOG_POSTS.find((post) => post.slug === slug);
}

export function getCategoryProducts(products, categorySlug) {
  const keys = categoryKeywords[categorySlug] || [];
  if (!keys.length) return products.slice(0, 6);

  const loweredKeys = keys.map((key) => key.toLowerCase());
  return products.filter((product) => {
    const blob = `${product.name} ${product.short} ${product.description}`.toLowerCase();
    return loweredKeys.some((keyword) => blob.includes(keyword));
  });
}

export function pickRelatedProducts(products, categorySlug, limit = 2) {
  const filtered = getCategoryProducts(products, categorySlug);
  if (filtered.length >= limit) return filtered.slice(0, limit);
  return products.slice(0, limit);
}
